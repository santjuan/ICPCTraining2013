	static class Matrix
	{
		static final long MOD = 1000000009;
		long[][] matrix;
		
		Matrix(long[][] m)
		{
			matrix = m;
		}
		
		Matrix multiply(Matrix other)
		{
			if(matrix[0].length != other.matrix.length)
				return null;
			int a = matrix.length;
			int b = other.matrix[0].length;
			int m = matrix[0].length;
			long[][] result = new long[matrix.length][other.matrix[0].length];
			for(int i = 0; i < a; i++)
				for(int j = 0; j < b; j++)
				{
					for(int k = 0; k < m; k++)
						result[i][j] += matrix[i][k] * other.matrix[k][j];
					result[i][j] %= MOD;
				}
			return new Matrix(result);
		}
		
		Matrix exponentiate(long exponent)
		{
			if(exponent <= 1)
				return this;
			Matrix half = exponentiate(exponent / 2);
			half = half.multiply(half);
			if((exponent & 1) == 1)
				half = half.multiply(this);
			return half;
		}
	}
	
	
	
	public static void main(String[] args)
	{
		Scanner sc = new Scanner();

		long n = sc.nextInt();
		long m = sc.nextInt();
		long k = sc.nextInt();
		if(m < k)
			System.out.println(m);
		else
		{
			long libres = n - m;
			long vecesPosibles = m / (k - 1);
			if(vecesPosibles < libres)
				System.out.println(m);
			else
			{
				long score = 0;
				long jugados = libres * (k - 1);
				m -= jugados;
				long veces = m / k;
				long restante = m % k;
				if(veces == 0)
					System.out.println((score + jugados + restante) % Matrix.MOD);
				else if(veces == 1)
					System.out.println(((score + k) * 2 + restante + jugados) % Matrix.MOD);
				else
				{
					Matrix base = new Matrix(new long[][]{{2, 0, 2}, {1, 0, 0}, {0, 0, 1}});
					Matrix ans = base.exponentiate(veces - 1).multiply(new Matrix(new long[][]{{(score + k) * 2}, {score}, {k}}));
					long total = ans.matrix[0][0];
					total += restante;
					total %= Matrix.MOD;
					System.out.println((total + jugados) % Matrix.MOD);
				}
			}
		}
	}
